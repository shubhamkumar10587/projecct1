// find size of datatype
#include<iostream>
using namespace std;
int main (){
    int intType;
    char charType;
    long longType;
    float floatType;
    double doubleType;
    short shortType;
    cout<<"size of intType :" <<sizeof(intType)<<" bytes \n\n";
    cout<<"size of charType :" <<sizeof(charType)<<" bytes \n\n";
    cout<<"size of shortType :" <<sizeof(shortType)<<" bytes \n\n";
    cout<<"size of longType :" <<sizeof(longType)<<" bytes \n\n";
    cout<<"size of floatType :" <<sizeof(floatType)<<" bytes \n\n";
    cout<<"size of doubleType :" <<sizeof(doubleType)<<" bytes \n\n";
}